#!/usr/bin/env python3
"""
FrameWorkers 环境安装脚本。
默认会：
1) 自动汇总各子目录 requirements.txt
2) 创建 conda 环境（已存在则跳过创建）
3) 在该环境内 pip 安装业务依赖 + 测试依赖（pytest）

兼容性：
- 会优先使用 `conda run --no-banner`；
- 若 conda 版本不支持该参数，会自动回退为不带 `--no-banner`。

用法（推荐先激活目标环境，再执行脚本；此时无需 ``conda`` 在 PATH 上）::

    conda activate frameworkers
    python install_requirements.py

说明：
- 默认会安装 `pytest`，无需额外参数。
- 若当前已 ``conda activate frameworkers``，直接用当前解释器执行 ``pip install``，不依赖 ``conda run``。
- 否则使用环境变量 ``CONDA_EXE`` 或 PATH 中的 ``conda`` 做 ``conda run`` / ``conda create``。
"""

import json
import os
import re
import shutil
import subprocess
import sys
from collections import defaultdict
from pathlib import Path

ENV_NAME = "frameworkers"
PYTHON_VERSION = "3.11"
SKIP_DIRS = {"node_modules", "__pycache__", ".git", ".venv", "venv", "env"}
OUTPUT_FILE = "requirements.txt"
TEST_PACKAGES = ["pytest"]


def find_requirements_files() -> list[Path]:
    root = Path(".").resolve()
    results = []
    for req_file in root.rglob("requirements.txt"):
        if req_file.resolve() == (root / OUTPUT_FILE).resolve():
            continue
        if any(part in SKIP_DIRS for part in req_file.parts):
            continue
        results.append(req_file)
    return sorted(results)


def parse_requirements(file_path: Path) -> list[str]:
    reqs = []
    with open(file_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            reqs.append(line)
    return reqs


def _pkg_name(req: str) -> str:
    m = re.match(r"^([A-Za-z0-9_][A-Za-z0-9._-]*)", req)
    return m.group(1).lower().replace("-", "_") if m else req.lower()


def merge_requirements(files: list[Path]) -> list[str]:
    merged: dict[str, str] = {}
    for fpath in files:
        for req in parse_requirements(fpath):
            pkg = _pkg_name(req)
            if pkg not in merged:
                merged[pkg] = req
            elif ">=" in req and "==" in merged[pkg]:
                merged[pkg] = req
    return list(merged.values())


def generate_requirements_txt(files: list[Path]):
    """Regenerate the unified requirements.txt from sub-directories."""
    root = Path(".").resolve()
    by_source: dict[str, list[str]] = defaultdict(list)
    seen: dict[str, str] = {}

    for fpath in files:
        rel = os.path.relpath(fpath, root)
        for req in parse_requirements(fpath):
            pkg = _pkg_name(req)
            if pkg not in seen:
                seen[pkg] = rel
                by_source[rel].append(req)
            elif ">=" in req and "==" in seen.get(pkg, ""):
                by_source[seen[pkg]] = [r for r in by_source[seen[pkg]] if _pkg_name(r) != pkg]
                seen[pkg] = rel
                by_source[rel].append(req)

    lines = [
        "# Auto-generated by install_requirements.py — do not edit manually.",
        "# Re-generate:  python install_requirements.py --generate",
        "",
    ]
    for source_rel in sorted(by_source):
        lines.append(f"# From {source_rel}")
        for req in sorted(by_source[source_rel]):
            lines.append(req)
        lines.append("")

    out_path = root / OUTPUT_FILE
    out_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {out_path}")


def _conda_executable() -> str | None:
    """``conda`` binary: ``CONDA_EXE`` (set after ``conda activate``) or PATH."""
    exe = os.environ.get("CONDA_EXE")
    if exe and Path(exe).is_file():
        return exe
    return shutil.which("conda")


def _running_inside_conda_env(env_name: str) -> bool:
    """True if the current interpreter is already that named conda env."""
    if os.environ.get("CONDA_DEFAULT_ENV") == env_name:
        return True
    prefix = os.environ.get("CONDA_PREFIX")
    if prefix and Path(prefix).name == env_name:
        return True
    return False


def conda_env_exists(env_name: str) -> bool:
    """Return True if a conda environment already exists (or we are already inside it)."""
    if _running_inside_conda_env(env_name):
        return True
    conda_exe = _conda_executable()
    if not conda_exe:
        return False
    try:
        result = subprocess.run(
            [conda_exe, "env", "list", "--json"],
            check=True,
            capture_output=True,
            text=True,
        )
        data = json.loads(result.stdout or "{}")
        envs = data.get("envs", [])
        for raw in envs:
            if Path(str(raw)).name == env_name:
                return True
            if str(raw).replace("\\", "/").endswith(f"/envs/{env_name}"):
                return True
        return False
    except Exception:
        return False


def install_requirements_into_target_env(env_name: str, reqs: list[str]) -> None:
    """
    Install packages into ``env_name``.

    If the active interpreter is already that env (e.g. after ``conda activate frameworkers``),
    use ``sys.executable -m pip`` so we never need ``conda`` on PATH.

    Otherwise use ``conda run -n <env> pip install ...`` with ``CONDA_EXE`` or ``conda``.
    """
    if _running_inside_conda_env(env_name):
        subprocess.run([sys.executable, "-m", "pip", "install", *reqs], check=True)
        return

    conda_exe = _conda_executable()
    if not conda_exe:
        print(
            "找不到 conda（且当前不在目标环境中）。请先执行：\n"
            f"  conda activate {env_name}\n"
            "或确保已安装 conda 并加入 PATH，或设置环境变量 CONDA_EXE。",
            file=sys.stderr,
        )
        raise SystemExit(1)

    cmd_with_banner_flag = [
        conda_exe,
        "run",
        "-n",
        env_name,
        "--no-banner",
        "pip",
        "install",
        *reqs,
    ]
    try:
        subprocess.run(
            cmd_with_banner_flag,
            check=True,
            capture_output=True,
            text=True,
        )
        return
    except subprocess.CalledProcessError as exc:
        err = (exc.stderr or "") + (exc.stdout or "")
        if "--no-banner" not in err:
            raise
        print("Detected older conda: retrying pip install without --no-banner ...")

    subprocess.run(
        [conda_exe, "run", "-n", env_name, "pip", "install", *reqs],
        check=True,
    )


def main() -> int:
    if "--help" in sys.argv or "-h" in sys.argv:
        print(
            "Usage:\n"
            "  python install_requirements.py              # one-click install (includes pytest)\n"
        )
        return 0

    if "--generate" in sys.argv:
        files = find_requirements_files()
        if not files:
            print("No sub-directory requirements.txt found.")
            return 1
        generate_requirements_txt(files)
        return 0

    files = find_requirements_files()
    if not files:
        print("No sub-directory requirements.txt found.")
        return 1

    reqs = merge_requirements(files)
    print(f"Collected {len(reqs)} packages from {len(files)} sub-directories.")

    if conda_env_exists(ENV_NAME):
        print(f"\nConda env '{ENV_NAME}' already exists, skipping create.")
    else:
        conda_exe = _conda_executable()
        if not conda_exe:
            print(
                "目标环境不存在，且找不到 conda 可执行文件。请先：\n"
                f"  conda create -n {ENV_NAME} python={PYTHON_VERSION} -y\n"
                f"  conda activate {ENV_NAME}\n"
                "然后重新运行本脚本。",
                file=sys.stderr,
            )
            return 1
        print(f"\nCreating conda env '{ENV_NAME}' (python={PYTHON_VERSION}) …")
        try:
            subprocess.run(
                [conda_exe, "create", "-n", ENV_NAME, f"python={PYTHON_VERSION}", "-y"],
                check=True,
            )
        except subprocess.CalledProcessError as exc:
            print(f"conda create failed (exit {exc.returncode})", file=sys.stderr)
            return 1

    if _running_inside_conda_env(ENV_NAME):
        print(f"\nInstalling {len(reqs)} packages into current env (active: {ENV_NAME}) …")
    else:
        print(f"\nInstalling {len(reqs)} packages into '{ENV_NAME}' …")
    try:
        install_requirements_into_target_env(ENV_NAME, reqs)
    except subprocess.CalledProcessError as exc:
        print(f"pip install failed (exit {exc.returncode})", file=sys.stderr)
        return 1

    print(f"\nInstalling test packages into '{ENV_NAME}' …")
    try:
        install_requirements_into_target_env(ENV_NAME, TEST_PACKAGES)
    except subprocess.CalledProcessError as exc:
        print(f"test package install failed (exit {exc.returncode})", file=sys.stderr)
        return 1

    print(f"\nDone! Run:\n  conda activate {ENV_NAME}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
